    private void botonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonUpdateActionPerformed
        String nombreViejo = textoNombreViejo.getText();
        String nuevoNombre = textoNombreNuevo.getText().isEmpty() ? nombreViejo : textoNombreNuevo.getText();
        String clase = listaClase.getSelectedValue();
        int nivel = Integer.parseInt(comboNiveles.getSelectedItem().toString());

        String nombreCampaña = listaCampanas.getSelectedValue(); // Solo nombre visible
        int idCampaña = -1;
        try {
            idCampaña = controlador.obtenerIdCampañaPorNombre(nombreCampaña); // AQUÍ USAS EL MÉTODO
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "No se pudo obtener el ID de la campaña", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            ModeloPersonaje personaje = new ModeloPersonaje();
            personaje.setNombrePersonaje(nuevoNombre);
            personaje.setClase(clase);
            personaje.setNivel(nivel);
            personaje.setId_campana(idCampaña);

            controlador.actualizarPersonaje(nombreViejo, personaje);

            JOptionPane.showMessageDialog(null, "Personaje actualizado correctamente.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al actualizar personaje.", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_botonUpdateActionPerformed


