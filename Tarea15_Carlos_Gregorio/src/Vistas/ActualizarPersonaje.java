    private void cargarDatosDesdeController(String nombre) {
        try {
            ModeloPersonaje personaje = controlador.obtenerPersonajePorNombre(nombre);
            if (personaje != null) {
                textoNombreViejo.setText(personaje.getNombrePersonaje());
                textoNombreViejo.setEditable(false);

                comboNiveles.setSelectedItem(String.valueOf(personaje.getNivel()));

                listaClase.setSelectedValue(personaje.getClase(), true);

                for (int i = 0; i < listaCampanas.getModel().getSize(); i++) {
                    String item = listaCampanas.getModel().getElementAt(i);
                    if (item.startsWith(personaje.getId_campana() + " -")) {
                        listaCampanas.setSelectedIndex(i);
                        break;
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Personaje no encontrado", "Aviso", JOptionPane.WARNING_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar personaje", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

    private void cargarCampanas() {
    DefaultListModel<String> modelo = new DefaultListModel<>();
    mapaCampanas.clear();
     try {
        Modelo.ConnectionBD conexionBD = new Modelo.ConnectionBD();
        conexionBD.connection();

        String sql = "SELECT id, nombre FROM partidas";
        ResultSet rs = conexionBD.getStatement().executeQuery(sql);

        while (rs.next()) {
            int id = rs.getInt("id");
            String nombre = rs.getString("nombre");

            modelo.addElement(nombre);                // Añade el nombre al modelo del JList
            mapaCampanas.put(nombre, id);             // Guarda el par nombre -> id
        }

        listaCampanas.setModel(modelo); // Asigna el modelo con los nombres al JList

        rs.close();
        conexionBD.closeConnection();

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al cargar campañas: " + ex.getMessage());
    }
}
